export default function DashboardTable() {
  var token = localStorage.getItem("rh_token");
  const [rows, setRows] = useState([]);
  useEffect(() => {
    fetch("http://localhost:2020/transactionhistory", {
      method: "GET",
      headers: {
        Accept: "application/form-data",
        Authorization: `Bearer ${token}`,
        "Content-Type": "application/json",
      },
    })
      .then((res) => res.json())
      .then((res) => {
        console.log(res);
        if (res && res.transactions && res.transactions.length > 0) {
          let rows_ = [];
          res.transactions.forEach((transaction) => {
            rows_.push(
              createData(
                "new Date()",
                transaction.transaction_id,
                transaction.rewards_earned,
                transaction.isUsed,
                transaction.store_id,
                transaction.store_name,
                transaction.purchase_amount
              )
            );
            setRows(rows_);
          });
        }
      })
      .catch((err) => {
        throw err;
      });
  }, []);

  return (
    <TableContainer component={Paper}>
      <Table aria-label="collapsible table">
        <TableHead>
          <TableRow>
            <TableCell />
            <TableCell>Date</TableCell>
            <TableCell align="right">transaction Id</TableCell>
            <TableCell align="right">Rewards</TableCell>
          </TableRow>
        </TableHead>
        <TableBody>
          {rows.map((row) => (
            <Row key={row.name} row={row} />
          ))}
        </TableBody>
      </Table>
    </TableContainer>
  );
}
